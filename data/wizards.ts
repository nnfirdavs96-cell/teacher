/**
 * Интерактивный мастер-решатель: дерево вопросов простыми словами.
 * Рассчитан на обычного человека без подготовки — жмёшь кнопки и получаешь
 * понятное решение по шагам. Узлы бывают «вопрос» и «результат».
 */

export interface WizardOption {
  label: string;
  next: string;
  hint?: string;
}

export interface WizardLink {
  label: string;
  /** Маршрут внутри приложения: /calculators, /reference, /article/<id> и т.п. */
  href: string;
}

export interface WizardNode {
  id: string;
  type: 'question' | 'result';
  title: string;
  text?: string;
  /** Для вопроса. */
  options?: WizardOption[];
  /** Для результата: шаги простыми словами. */
  steps?: string[];
  severity?: 'info' | 'good' | 'warn' | 'bad';
  links?: WizardLink[];
}

export interface Wizard {
  id: string;
  title: string;
  subtitle: string;
  icon: string;
  color: string;
  start: string;
  nodes: Record<string, WizardNode>;
}

